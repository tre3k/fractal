#include "mainwindow.h"
#include <QApplication>
#include <QTranslator>
#include <QLocale>

int main(int argc, char *argv[])
{
    QApplication a(argc, argv);

    QTranslator translator;
    if(translator.load("langs/fractal_" + QLocale::system().name())) {
	    a.installTranslator(&translator);
    }

    MainWindow w;
    w.show();

    return a.exec();
}
